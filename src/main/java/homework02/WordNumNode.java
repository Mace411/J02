package homework02;

public class WordNumNode {
	private String word;
	private int num;

	public WordNumNode() {
	}
	
	public WordNumNode(String word, int num) {
		super();
		this.word = word;
		this.num = num;
	}

	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
